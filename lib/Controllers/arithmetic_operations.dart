

class ArithmaticOperation{

  static double  add(double first_operand, double second_operand){
    return first_operand + second_operand;
  }
  static double subtract(double first_operand, double second_operand){
    return first_operand - second_operand;
  }
  static double multiply(double first_operand, double second_operand){
    return first_operand * second_operand;
  }
  static double divide(double first_operand, double second_operand){
    return first_operand / second_operand;
  }

}